const getInitialLocale = () => {
    if (typeof window === 'undefined') {
        return 'es';
    }

    return window.localStorage.getItem('servicio-linea-once.locale') || 'es';
};

const locale = ref(getInitialLocale());

watch(locale, (value) => {
    if (typeof window === 'undefined') {
        return;
    }

    window.localStorage.setItem('servicio-linea-once.locale', value);
    document.documentElement.lang = value;
}, { immediate: true });

export function useLanguage() {
    const syncBackendLocale = (value) => {
        if (typeof window === 'undefined') {
            return;
        }

        const token = document.querySelector('meta[name="csrf-token"]')?.getAttribute('content');

        fetch(`/locale/${value}`, {
            method: 'POST',
            keepalive: true,
            headers: {
                Accept: 'application/json',
                'X-Requested-With': 'XMLHttpRequest',
                ...(token ? { 'X-CSRF-TOKEN': token } : {}),
            },
        }).catch(() => {});
    };

    const setLocale = (value) => {
        locale.value = value;
        syncBackendLocale(value);
    };

    const t = (key, replacements = {}) => {
        const line = dictionaries[locale.value]?.[key] || dictionaries.es[key] || key;

        return Object.entries(replacements).reduce(
            (text, [name, value]) => text.replaceAll(`{{${name}}}`, value),
            line,
        );
    };

    return { locale, setLocale, t };
}
